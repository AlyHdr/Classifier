    private void btn_find_athletesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_find_athletesActionPerformed
        // TODO add your handling code here:
        
        ArrayList<Double> attrs=new ArrayList<>();
        attrs.add(Double.parseDouble(text_height.getText()));
        attrs.add(Double.parseDouble(text_weight.getText()));
        Set set=new Set(attrs);
        String rs="";
        String selected=(String)combo_athletes.getSelectedItem();
        switch(selected)
        {
            case "Distance" :
            {
                rs=methods.distance(set, db.getAthletesTraining());
                break;
            }
            case "Norm" :
            {
                rs=methods.Normalization(set, db.getAthletesTraining());
                break;
            }
            case "MSS" :
            {
                rs=methods.MSS(set, db.getAthletesTraining());
                break;
            }
            case "KNN" :
            {
                rs=methods.KNN(set, db.getAthletesTraining(),3);
                break;
            }
            case "Bayes" :
            {
                rs=methods.Bayes(set, db.getAthletesTraining());
                break;
            }
        }
        label_athletes.setText(rs);
    }//GEN-LAST:event_btn_find_athletesActionPerformed


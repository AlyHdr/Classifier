    private void btn_distanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_distanceActionPerformed
        double counter=0;
        for(Set test:tests)
        {
            if(methods.distance(test, trainings).equals(test.getClas().trim()))
                counter++;
        }
        double res=(counter/tests.size())*100;
        label_accuracy.setText("accuracy:"+res+" %");
        
    }//GEN-LAST:event_btn_distanceActionPerformed

